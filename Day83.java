public class Day83 {
    public static void main(String[] args) {
        int [] a ={1,2,3,4,5};
        int hasil = 0;
        
        for (int i = 0; i < a.length; i++) {
            hasil += a[i];
        }
        System.out.println("Hasil penjumlahann : "+hasil);
    }
    
}
