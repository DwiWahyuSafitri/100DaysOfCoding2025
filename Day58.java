package pkg100dayscoding;

public class Day58 {
    public static void main(String[] args) {
       int n = 5;
       
        for (int i = 1; i <= n ; i++) {
            System.out.println(i);
        }
        
        for (int i = n; i >= 1; i--) {
            System.out.println(i);
        }
        
    }
    
}
